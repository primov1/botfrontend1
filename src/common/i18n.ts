export function normalizeLang(code?: string | null): Lang {
    const c = (code ?? '').toLowerCase();
    if (c === 'ru' || c.startsWith('ru')) return 'ru';
    if (c === 'en' || c.startsWith('en')) return 'en';
    return 'uz';
}

